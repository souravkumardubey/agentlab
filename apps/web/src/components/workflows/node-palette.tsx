export function NodePalette() {
  const onDragStart = (event: DragEvent, nodeType: string) => {
    event.dataTransfer.setData('application/reactflow', nodeType);
    event.dataTransfer.effectAllowed = 'move';
  };

  return (
    <div className="space-y-2">
      <h3 className="text-xs font-semibold uppercase tracking-wider mb-3" style={{ color: 'var(--text-muted)' }}>
        Drag nodes
      </h3>
      {NODE_TYPES.map((node) => (
        <div
          key={node.type}
          draggable
          onDragStart={(e) => onDragStart(e, node.type)}
          className="flex items-center gap-3 px-3 py-2.5 rounded-lg cursor-grab active:cursor-grabbing transition-all hover:scale-[1.02]"
          style={{
            background: node.bg,
            border: `1px solid ${node.color}20`,
          }}
        >
          <div className="w-8 h-8 rounded-lg flex items-center justify-center" style={{ color: node.color }}>
            {node.icon}
          </div>
          <div>
            <div className="text-sm font-medium" style={{ color: 'var(--text-primary)' }}>{node.label}</div>
            <div className="text-xs" style={{ color: 'var(--text-muted)' }}>{node.description}</div>
          </div>
        </div>
      ))}
    </div>
  );
}
